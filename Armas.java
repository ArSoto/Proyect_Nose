
public class Armas {
    private int balas;
    private String tipo;


    public Armas(int balas, String tipo) {
        this.balas = balas;
        this.tipo = tipo;

    }

    public void setBalas(int balas) {
        this.balas = balas;
    }


    public int getBalas() {
        return balas;
    }

    public String getTipo() {
        return tipo;
    }




}

